package com.indianic.loginmodule.Login;

class UserModel {
    private String id;
    private String name;
    private String nick;

    public String getId() {
        return id;
    }

    public void setId(String mId) {
        id = mId;
    }

    public String getName() {
        return name;
    }

    public void setName(String mName) {
        name = mName;
    }

    public String getNick() {
        return nick;
    }

    public void setNick(String mNick) {
        nick = mNick;
    }
}
